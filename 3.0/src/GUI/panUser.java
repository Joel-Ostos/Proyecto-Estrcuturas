    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
        if(cmbFiltro.getSelectedIndex() == 0){ //buscar por nombre

            limpiarTabla();

            String nombre = txtBusqueda.getText();
            txtBusqueda.setText("");

            Libro l = Exe.almacen.mostrarPorNombre(nombre);
            if(l == null){
                lblInfB.setText("No se encuentra el libro");
            }
            else{
                tblModel.addRow(l.toArray());
            }

        }
        if(cmbFiltro.getSelectedIndex() == 1){ // buscar por autor
            limpiarTabla();

            String nombre = txtBusqueda.getText();
            txtBusqueda.setText("");

            Exe.listaSalida = Exe.almacen.mostrarPorAutor(nombre);

            for(int i = 0; i < Exe.listaSalida.size(); i++){
                Libro l = Exe.listaSalida.get(i);
                tblModel.addRow(l.toArray());
            }
            Exe.listaSalida.clear();
        }

        if(cmbFiltro.getSelectedIndex() == 2){

            limpiarTabla();

            String nombre = txtBusqueda.getText();
            txtBusqueda.setText("");

            Exe.listaSalida = Exe.almacen.mostrarPorEditorial(nombre);

            for(int i = 0; i < Exe.listaSalida.size(); i++){
                Libro l = Exe.listaSalida.get(i);
                tblModel.addRow(l.toArray());
            }
            Exe.listaSalida.clear();
        }

        if(cmbFiltro.getSelectedIndex() == 3){
            limpiarTabla();

            String nombre = txtBusqueda.getText();
            txtBusqueda.setText("");

            Exe.listaSalida = Exe.almacen.mostrarPorCategoria(nombre);

            for(int i = 0; i < Exe.listaSalida.size(); i++){
                Libro l = Exe.listaSalida.get(i);
                tblModel.addRow(l.toArray());
            }
            Exe.listaSalida.clear();
        }

        if(cmbFiltro.getSelectedIndex() == 4){
            limpiarTabla();

            Exe.listaSalida = Exe.almacen.mostrarTodos();

            for(int i = 0; i < Exe.listaSalida.size(); i++){
                Libro l = Exe.listaSalida.get(i);
                tblModel.addRow(l.toArray());
            }
        }
    }//GEN-LAST:event_btnBuscarMouseClicked

    private void btnSolicitarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSolicitarMouseClicked
        // TODO add your handling code here:
        
        if(tblInventario.getSelectedRowCount() != 1){
            return;
        }
        int fila = tblInventario.getSelectedRow();        
        String nombreSolicitud = (String) tblModel.getValueAt(fila,0);  
        int cantidad = (int) tblModel.getValueAt(fila,2); 
        boolean existencia = true;
        Libro l = Exe.almacen.mostrarPorNombre(nombreSolicitud); 
        
        if(cantidad > 0 && true){             
            //yo se que es redundante mostrar dos veces el arbol, podria simplemente editar la tabla y luego el arbol pero es por seguir la filosofia de primero arbol, luego mostrar datos al editar
           
            lblSolicitud.setText("Solicitar");  
            existencia = Exe.cliente.añadirLibro(l);
            
            if(existencia == false){
                //ya esta el libro solicitado
                lblInfR.setText("Ya solicito el libro.");
                
            }
            else{
                Exe.almacen.retirarUnidad(nombreSolicitud);
                lblInfR.setText("Ha solicitado el libro exitosamente.");
                Libro l2 = Exe.almacen.mostrarPorNombre(nombreSolicitud);            
                tblInventario.setValueAt(l2.getCantidad(),fila,2);
            } 
        }
        if(cantidad == 0 && Exe.cliente.buscarLibro(l) != l ){ //totalmente funcional, ya si el libro lo pidio, no lo puede reservar
            
            lblSolicitud.setText("Reservar");
            int condReservar = Exe.almacen.reservarLibro(Exe.cliente.getNombre(), nombreSolicitud); 
            switch(condReservar) {
                case 0:
                    lblInfR.setText("El libro buscado no existe.");
                    break;
                case 1:
                    lblInfR.setText("Ya tiene reservado el libro. Espere a que este disponible.");
                    break;
                case 2:
                    lblInfR.setText("Ha sido añadido a la cola de espera exitosamente.");     
                    break;
            }
        }
        
        

        //tblModel.removeRow(fila);
    }//GEN-LAST:event_btnSolicitarMouseClicked

    private void btnCancelarReservacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCancelarReservacionMouseClicked
        if(tblInventario.getSelectedRowCount() != 1){
            return;
        }
        int fila = tblInventario.getSelectedRow();        
        String nombreCancelacion = (String) tblModel.getValueAt(fila,0);  
        Exe.almacen.cancelarReserva(Exe.cliente.getNombre(), nombreCancelacion);
        
    }//GEN-LAST:event_btnCancelarReservacionMouseClicked

    private void tblInventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblInventarioMouseClicked
        if(evt.getClickCount() == 1 || evt.getClickCount() == 2 ){
            int fila = tblInventario.getSelectedRow();             
            int cantidad = (int) tblModel.getValueAt(fila,2); 
            if(cantidad > 0){
                btnSolicitar.setBackground(Color.BLUE);
                lblSolicitud.setText("Solicitar");
                btnCancelarReservacion.setVisible(false);
            }
            if(cantidad == 0){
                btnSolicitar.setBackground(Color.RED);                
                lblSolicitud.setText("Reservar");
                btnCancelarReservacion.setVisible(true);
                
            }
            else{
                
            }
        }   
    }//GEN-LAST:event_tblInventarioMouseClicked

